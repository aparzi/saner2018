                <p>
                    <b>SANER</b> is the premier research conference on the theory and practice of recovering information
                    from existing software and systems.
                    It explores innovative methods of extracting the many kinds of information that can be recovered
                    from software, software engineering documents, and systems artefacts, and examines innovative ways
                    of using this information in system renovation and program understanding.
                </p>
                <p>
                    <b>SANER</b> promotes discussion and interaction among researchers and practitioners about the
                    development of maintainable systems, and the improvement, evolution, migration, and reengineering of
                    existing systems.
                    It also explores innovative methods of extracting the many kinds of information of interest to
                    software developers and examines innovative ways of using this information in system renovation and
                    program understanding.
                </p>
                <p>
                    <b>SANER</b> will feature technical research paper sessions, keynotes from outstanding and
                    world-wide known speakers, workshops, an early research achievements track, an industry paper track
